
CREATE TABLE LDC_PROGRAMADOR(PGIDENT	NUMBER(15) NOT NULL,
                            PGPRNOM	VARCHAR2(100) NOT NULL,
                            PGSENOM	VARCHAR2(100) ,
                            PGPRAPE	VARCHAR2(100) NOT NULL,
                            PGSEAPE	VARCHAR2(100) ,
                            PGEDAD	NUMBER(3),
                            PGANEXP	NUMBER(3),
                            PGSEXO	VARCHAR2(1),
                            PGUSUAR	VARCHAR2(100),
                            PGFEACT	DATE);
ALTER TABLE LDC_PROGRAMADOR ADD CONSTRAINT PK_LDC_PROGRAMADOR PRIMARY KEY(PGIDENT);
ALTER TABLE LDC_PROGRAMADOR ADD CONSTRAINT CK_PROGRAMADOR_SEXO CHECK (PGSEXO = 'M' OR  PGSEXO = 'F');
COMMENT ON COLUMN LDC_PROGRAMADOR.PGIDENT IS 'Identificador del programador';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGPRNOM IS 'Primer nombre';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGSENOM IS 'Segundo nombre';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGPRAPE IS 'Primer apellido';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGSEAPE IS 'Segundo apellido';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGEDAD IS 'Edad';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGANEXP IS 'Año de experiencia';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGSEXO IS 'Sexo [M – Masculino, F - Femenino]';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGUSUAR IS 'Usuario';
COMMENT ON COLUMN LDC_PROGRAMADOR.PGFEACT IS 'Fecha de Actualizacion';
COMMENT ON TABLE LDC_PROGRAMADOR IS 'Programadores';   
/
CREATE SEQUENCE SEQ_LDC_PROGRAMADOR START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
/


CREATE TABLE LDC_LENGUPROG(LPIDENT	NUMBER(15) NOT NULL,
