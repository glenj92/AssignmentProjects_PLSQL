COMMENT ON COLUMN LDC_LENGUPROG.LPIDENT  IS 'Identificador de Lenguaje de programacion';
COMMENT ON COLUMN LDC_LENGUPROG.LPNOMBR  IS 'Nombre';
COMMENT ON COLUMN LDC_LENGUPROG.LPDESCR  IS 'Descripcion';
COMMENT ON COLUMN LDC_LENGUPROG.LPESTAD  IS 'Estado [A – Activo, I - Inactivo]';
COMMENT ON COLUMN LDC_LENGUPROG.LPUSUAR  IS 'Usuario';
COMMENT ON COLUMN LDC_LENGUPROG.LPFEACT  IS 'Fecha de Actualizacion';
COMMENT ON TABLE LDC_LENGUPROG IS 'Lenguajes de programacion';
/
CREATE SEQUENCE SEQ_LDC_LENGUPROG START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
/

CREATE TABLE LDC_PROYECTO(PYIDENT	NUMBER(15) NOT NULL,
                            PYNOMBR	VARCHAR2(100) NOT NULL,
                            PYVALOR	NUMBER(15,2) ,
                            PYFEINI	DATE,
                            PYFEFIN	DATE,
                            PYCAPRO	NUMBER(5),  
                            PYLEPRO	NUMBER(15),
                            PYESTAD	VARCHAR2(1),
                            PYUSUAR	VARCHAR2(100),
                            PYFEACT	DATE);
ALTER TABLE LDC_PROYECTO ADD CONSTRAINT PK_LDC_PROYECTO PRIMARY KEY(PYIDENT);
ALTER TABLE LDC_PROYECTO ADD CONSTRAINT CK_PROYECTO_VALFECHA CHECK (PYFEINI < PYFEFIN);
ALTER TABLE LDC_PROYECTO ADD CONSTRAINT CK_PROYECTO_ESTADO CHECK (PYESTAD = 'V' OR  PYESTAD = 'T');
ALTER TABLE LDC_PROYECTO ADD CONSTRAINT FK_PROYECTO_LENGUPROG FOREIGN KEY (PYLEPRO) REFERENCES LDC_LENGUPROG(LPIDENT);
COMMENT ON COLUMN LDC_PROYECTO.PYIDENT IS 'Identificador de proyecto';
COMMENT ON COLUMN LDC_PROYECTO.PYNOMBR IS 'Nombre del proyecto';
COMMENT ON COLUMN LDC_PROYECTO.PYVALOR IS 'Valor del proyecto';
COMMENT ON COLUMN LDC_PROYECTO.PYFEINI IS 'Fecha de inicio';
COMMENT ON COLUMN LDC_PROYECTO.PYFEFIN IS 'Fecha de finalizacion';
COMMENT ON COLUMN LDC_PROYECTO.PYCAPRO IS 'Cantidad de programadores';
COMMENT ON COLUMN LDC_PROYECTO.PYLEPRO IS 'Lenguaje de programacion';
COMMENT ON COLUMN LDC_PROYECTO.PYESTAD IS 'Estado [V – Vigente, T - Terminado]';
COMMENT ON COLUMN LDC_PROYECTO.PYUSUAR IS 'Usuario';
COMMENT ON COLUMN LDC_PROYECTO.PYFEACT IS 'Fecha de Actualizacion';
COMMENT ON TABLE LDC_PROYECTO IS 'Proyectos';
/
CREATE SEQUENCE SEQ_LDC_PROYECTO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
/

